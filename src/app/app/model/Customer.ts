export class Customer{
     name:String;
     contactNumber:Number;
     address:String;
     email:String;
     password:String;

     constructor(name:String,contactNumber:Number,address:String,email:String,password:String){
          this.name=name;
          this.contactNumber=contactNumber;
          this.address=address;
          this.email=email;
          this.password=password;
     }

}